const mongoose = require('mongoose');

// Schema
const Loginschema = new mongoose.Schema({
    fullname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    phone: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    userType: {
        type: String,
        required: true
    }
});

// Collection part
const UserModel = mongoose.model("users", Loginschema);

module.exports = UserModel;